//boolean : 논리형

package com.javaex.ex01;

public class Ex03 {
	public static void main(String[] args) {
		boolean b01 = true;
		boolean b02 = false;
		
		System.out.println(b01);
		System.out.println(b02);
		
		int a = 3;
		int b = 5;
		
		//결과값을 true or false로 결과가 나오길 원할 때 boolean 사용 
		boolean result = a>b;
		System.out.println(result);
		
		
		
	}
}
